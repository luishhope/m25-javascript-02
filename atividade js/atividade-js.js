//laço for
for (let i=0; i<11; i++) {
    console.log ('contando até o numero 10: ' + i)
}
console.log('fim do for')
//while
let i=1;
while (i<11) {
    console.log('valor de i=' + i++);
}
console.log('fim do while');
//do, while
i=1;
do{
    console.log('valor de i=' + i++);
} while (i<11);
console.log('fim do while');